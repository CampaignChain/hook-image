namespace CampaignChain\Hook\ImageBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Class Image
 * @package CampaignChain\Hook\ImageBundle\EntityService
 *
 * @ORM\Entity()
 * @ORM\Table(name="campaignchain_hook_image")
 */
class Image
{
    /**
     * @ORM\Column(type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    protected $id;

    /**
     * @ORM\Column(type="string")
     */
    protected $path;

    /**
     * @ORM\ManyToOne(targetEntity="CampaignChain\CoreBundle\Entity\Activity", inversedBy="images")
     * @ORM\JoinColumn(referencedColumnName="id")
     */
    protected $activity;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getPath()
    {
        return $this->path;
    }

    /**
     * @param mixed $path
     */
    public function setPath($path)
    {
        $this->path = $path;
    }

    /**
     * @return mixed
     */
    public function getActivity()
    {
        return $this->activity;
    }

    /**
     * @param mixed $activity
     */
    public function setActivity($activity)
    {
        $this->activity = $activity;
    }
}
